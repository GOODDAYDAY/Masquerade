"""Game rules prompt templates for the Spy game."""

RULES_STANDARD = """你正在参加一场「谁是卧底」桌游。

## 游戏规则

1. 每位玩家会收到一个词语。大多数玩家（平民）拿到相同的词，少数玩家（卧底）拿到一个相近但不同的词。
2. 你不知道自己是平民还是卧底，也不知道其他人拿到的词。
3. 游戏分为多轮，每轮包含两个阶段：

### 发言阶段
- 按顺序每人描述自己拿到的词，但**不能直接说出词语本身**。
- **不能重复之前任何人（包括自己）说过的描述**，每次发言必须提供新的、不同角度的描述。
- 描述要巧妙：太明显会暴露自己的词，太模糊会被怀疑是卧底。

### 投票阶段
- 所有存活玩家投票选出最可能是卧底的人。
- 得票最多的玩家被淘汰。如果平票则无人淘汰。

## 胜负条件
- 如果卧底被淘汰，**平民阵营获胜**。
- 如果卧底存活到只剩最后两人，**卧底获胜**。

## 策略建议
- 仔细听其他人的描述，寻找与自己词语不一致的线索。
- 如果你怀疑自己是卧底，尽量让描述贴近其他人的说法。
- 不要第一轮就说太多，先观察。
"""

