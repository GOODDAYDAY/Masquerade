"""Game rules prompt templates for the Werewolf game."""

WEREWOLF_RULES = """你正在参加一场「狼人杀」桌游。

## 角色与阵营

**狼人阵营：**
- 狼人(Werewolf)：每晚可以集体选择击杀一名玩家。狼人之间知道彼此身份。

**好人阵营：**
- 村民(Villager)：没有特殊能力，依靠逻辑推理投票。
- 预言家(Seer)：每晚可以查验一名玩家的阵营（好人/狼人）。
- 女巫(Witch)：拥有一瓶解药（救人）和一瓶毒药（杀人），整局各限用一次，同一晚不能同时使用。
- 猎人(Hunter)：死亡时可以选择开枪带走一名存活玩家。
- 守卫(Guard)：每晚可以保护一名玩家免受狼人袭击，不能连续两晚保护同一人。

## 游戏流程

每轮包含夜晚和白天两个阶段：

### 夜晚阶段
- **重要：夜晚不能说话！所有交流只能通过肢体动作、手势、眼神来表达。**
- 行动顺序：守卫 → 狼人 → 女巫 → 预言家
- 守卫选择保护目标（动作描述）
- 狼人进行2轮内部动作交流（仅狼人可见），然后选择击杀目标
- 女巫得知被杀者，决定是否用药（动作描述）
- 预言家选择查验目标（动作描述）
- 夜晚结束后统一宣布死亡玩家（不公布死因）

### 白天阶段
- 宣布昨晚死亡玩家
- 死亡玩家可以发表遗言（猎人可选择开枪）
- 存活玩家依次发言讨论
- 所有存活玩家投票放逐一人
- 平票则无人被放逐

## 胜负条件
- **好人阵营胜利：** 所有狼人被淘汰
- **狼人阵营胜利：** 存活狼人数量 ≥ 存活好人数量

## 特殊规则
- 守卫不能连续两晚保护同一人
- 女巫的解药和毒药整局各限用一次，且同一晚不能同时使用
- 守卫保护和女巫解药不叠加（同时触发则目标仍然死亡）
- 猎人仅在死亡时可以选择是否开枪

## 本局禁止事项
- **没有警长竞选，不存在警徽流，不要提及警徽流**
- **玩家没有编号，只有名字。必须使用玩家的真实名字（如"甄暴躁""甄冷静"），绝对不能使用"1号""3号""玩家B"等编号或代号**
- **操作类型必须使用英文原文（如 speak, vote, protect, wolf_discuss, wolf_kill, witch_action, seer_check, last_words, hunter_shoot），不能翻译成中文**
"""


def get_rules_prompt() -> str:
    """Return the werewolf game rules prompt."""
    return WEREWOLF_RULES
